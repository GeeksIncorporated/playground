class Solution:
    # @param A : tuple of integers
    # @param B : integer
    # @return an integer

    def find_pivot(self, A):
        l = 0
        r = len(A) - 1
        while l < r - 1:
            m = int(l + (r - l) / 2)
            if A[m] < A[l]:
                r = m
            elif A[m] > A[r]:
                l = m
            elif A[m] > A[l]:
                l = m
            elif A[m] < A[r]:
                l = m
        return r

    def bs(self, A, B):
        l, r = 0, len(A)
        while l < r:
            m = int(l + (r - l) / 2)
            if A[m] == B:
                return m

            elif A[m] > B:
                r = m
            else:
                l = m + 1
        return -1

    def search(self, A, B):
        p = self.find_pivot(A)
        if B == A[p]:
            return p
        if B > A[p - 1] or B < A[0]:
            res = self.bs(A[p:], B)
            if res == -1:
                return -1
            else:
                return p + res
        else:
            return self.bs(A[:p], B)


