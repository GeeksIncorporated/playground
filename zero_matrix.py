# Given an m x n matrix of 0s and 1s, if an element is 0, set its entire row and column to 0.
# Do it in place.
#

class Solution:
    # @param A : list of list of integers
    # @return the same list modified

    def set_zeros(A, a, b):
        for i in len(A):
            A[a][i] = 0
        for i in len(A):
            A[i][b] = 0
        return A

    def setZeroes(self, A):

        update_first_raw = filter(lambda x: x == 0, [c[0] for c in A])
        update_first_column = 0 in A[0]

        for i in xrange(1, len(A)):
            for j in xrange(1, len(A[0])):
                if A[i][j] == 0:
                    A[0][j] = 0
                    A[i][0] = 0

        for i in xrange(1, len(A)):
            if A[i][0] == 0:
                for j in xrange(1, len(A[0])):
                    A[i][j] = 0

        for i in xrange(1, len(A[0])):
            if A[0][i] == 0:
                for j in xrange(1, len(A)):
                    A[j][i] = 0

        if update_first_column:
            for i in xrange(len(A[0])):
                A[0][i] = 0

        if update_first_raw:
            for i in xrange(len(A)):
                A[i][0] = 0

        return A

