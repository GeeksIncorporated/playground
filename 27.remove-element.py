# @lc code=start
class Solution:
    def removeElement(self, nums, val) -> int:
        l = 0
        r = len(nums) - 1
        
        while l <= r:
            if nums[l] == val:
                nums[l], nums[r] = nums[r], nums[l]
                r -= 1
            else:
                l += 1
        return r + 1

# @lc code=end

